let hideText_btn01 = document.getElementById('hideText_btn01');
let hideText01 = document.getElementById('hideText01');

hideText_btn01.addEventListener('click', toggleText01);

function toggleText01() {
    hideText01.classList.toggle('show');
    
    if(hideText01.classList.contains('show')){
        hideText_btn01.innerHTML="Leer menos";
    }else{
        hideText_btn01.innerHTML="Leer mas";
    }
}


